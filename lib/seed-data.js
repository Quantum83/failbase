export function getAvatarUrl(seed) {
  return `https://api.dicebear.com/8.x/notionists/svg?seed=${seed}&backgroundColor=b6e3f4,c0aede,d1d4f9,ffd5dc,ffdfbf`
}

export function getTotalReactions(reactions) {
  return Object.values(reactions).reduce((sum, v) => sum + v, 0)
}

export function getTopReaction(reactions) {
  return Object.entries(reactions).sort(([,a], [,b]) => b - a)[0][0]
}

export function formatNumber(n) {
  if (n >= 1000) return `${(n / 1000).toFixed(1)}k`
  return n.toString()
}

export function getFailureScore(post) {
  return getTotalReactions(post.reactions) + post.comments_count * 3
}

export function getRankedPosts() {
  return [...SEED_POSTS].sort((a, b) => getFailureScore(b) - getFailureScore(a))
}

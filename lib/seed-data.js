/**
 * Date-seeded pseudo-random selection from an array.
 * Returns `count` items, consistent for the entire day.
 * Use different `offset` values for independent selections on the same day.
 */
export function getDateSeeded(array, count, offset = 0) {
  const today = new Date();
  const daySeed =
    today.getFullYear() * 10000 +
    (today.getMonth() + 1) * 100 +
    today.getDate() +
    offset;
  const shuffled = [...array];
  let s = daySeed;
  for (let i = shuffled.length - 1; i > 0; i--) {
    s = (s * 1103515245 + 12345) & 0x7fffffff;
    const j = s % (i + 1);
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled.slice(0, count);
}

export function getSeedLeaderboard() {
  const userScores = {};

  SEED_POSTS.forEach((post) => {
    if (!userScores[post.author_id]) {
      const profile = getProfile(post.author_id);
      userScores[post.author_id] = {
        ...profile,
        posts: 0,
        totalReactions: 0,
        totalComments: 0,
        totalScore: 0,
      };
    }
    userScores[post.author_id].posts += 1;
    userScores[post.author_id].totalReactions += getTotalReactions(
      post.reactions,
    );
    userScores[post.author_id].totalComments += post.comments_count || 0;
    userScores[post.author_id].totalScore += getFailureScore(post);
  });

  return Object.values(userScores).sort((a, b) => b.totalScore - a.totalScore);
}
